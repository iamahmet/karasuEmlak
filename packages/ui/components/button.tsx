import * as React from 'react';
import { Slot } from '@radix-ui/react-slot';
import { cva, type VariantProps } from 'class-variance-authority';
import { cn } from '@karasu/lib';

const buttonVariants = cva(
  'inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium transition-all duration-300 ease-out focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 min-h-[44px] min-w-[44px] micro-interaction', // Minimum click target 44x44px + micro-interaction
  {
    variants: {
      variant: {
        default: 'bg-primary text-primary-foreground hover:bg-primary-dark shadow-sm hover:shadow-glow active:scale-95',
        destructive: 'bg-destructive text-destructive-foreground hover:bg-destructive/90 active:scale-95',
        outline: 'border-2 border-primary bg-background text-primary hover:bg-primary-subtle hover:border-primary-dark active:scale-95',
        secondary: 'bg-secondary text-secondary-foreground hover:bg-secondary/80 active:scale-95',
        ghost: 'hover:bg-primary-subtle hover:text-primary active:scale-95',
        link: 'text-primary underline-offset-4 hover:underline p-0 min-h-0 min-w-0',
      },
      size: {
        default: 'h-11 px-6 py-2.5', // Increased padding for better clickability
        sm: 'h-10 px-4 py-2 rounded-md',
        lg: 'h-12 px-8 py-3 rounded-md text-base',
        icon: 'h-11 w-11', // Minimum 44x44px
      },
    },
    defaultVariants: {
      variant: 'default',
      size: 'default',
    },
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, disabled, ...props }, ref) => {
    const Comp = asChild ? Slot : 'button';
    return (
      <Comp 
        className={cn(
          buttonVariants({ variant, size }),
          'focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-primary',
          'active:scale-[0.98]',
          disabled && 'opacity-50 cursor-not-allowed pointer-events-none',
          className
        )} 
        ref={ref} 
        disabled={disabled}
        {...props} 
      />
    );
  }
);
Button.displayName = 'Button';

export { Button, buttonVariants };

